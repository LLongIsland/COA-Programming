package cpu.alu;

import org.junit.Test;
import static org.junit.Assert.assertEquals;

public class NBCDUTest {

	private NBCDU nbcdu = new NBCDU();

	@Test
	public void testAdd1() {
		assertEquals("11000000000000000000000101110111", nbcdu.add("11000000000000000000000010011000", "11000000000000000000000001111001"));
	}

	@Test
	public void testAdd2() {
		assertEquals("11001000011101100000000000000000", nbcdu.add("11001001001110000000000000000000", "11001001001110000000000000000000"));
	}

	@Test
	public void testAdd3() {
		assertEquals("11010000000000000000000101110111", nbcdu.add("11010000000000000000000010011000", "11010000000000000000000001111001"));
	}

	@Test
	public void testAdd4() {
		assertEquals("11010000000000000000000000010001", nbcdu.add("11010000000000000000000000111001", "11000000000000000000000000101000"));
	}

	@Test
	public void testSub1() {
		assertEquals("11000000000000000000000110000100", nbcdu.sub("11000000000000000000000100100101", "11000000000000000000001100001001"));
	}

	@Test
	public void testSub2() {
		assertEquals("11010000000000000000000110000100", nbcdu.sub("11000000000000000000001100001001", "11000000000000000000000100100101"));
	}

	@Test
	public void testSub3() {
		assertEquals("11000000000000000000000000000000", nbcdu.sub("11000000000000000000000000000000", "11000000000000000000000000000000"));
	}

	@Test
	public void testSub4() {
		assertEquals("11000000000000000000000000000000", nbcdu.sub("11010000000000000000100010011000", "11010000000000000000100010011000"));
	}


}
